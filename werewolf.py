        # ç™½æ¿å‡ºå±€ï¼Œå§åº•å·²æ— å½±å“ï¼ˆæˆ–æ²¡æœ‰å§åº•ï¼‰
        await announce_elimination(eliminated_uid, chat_id, context, is_game_end=True)
        return
